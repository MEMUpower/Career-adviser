def calculate_recommendation_scores(
    competency_scores: Dict[str, float],
    weights: Dict[str, float],
    metric_modifiers: Dict[str, float]
) -> Dict[str, float]:
    """
    Algorithmic calculation of candidate scores based on weighted capabilities and public metrics.
    For MVP, we support configurable weights as fallback/pre-score filtering.
    """
    scores = {}
    base_scores = {
        "컴퓨터공학과": 80.0,
        "전자공학과": 75.0,
        "소프트웨어 엔지니어": 82.0,
        "데이터 과학자": 78.0
    }
    
    # Calculate score modifier from competency weights
    modifier = 0.0
    for key, weight in weights.items():
        if key in competency_scores:
            modifier += (competency_scores[key] / 100.0) * weight
            
    # Combine with metric modifiers (e.g. higher employment rate adds boost)
    for major, base in base_scores.items():
        metric_bonus = metric_modifiers.get(major, 5.0) * weights.get("취업률가중치", 0.15)
        scores[major] = round(base + (modifier * 20.0) + metric_bonus, 2)
        
    return scores

def validate_evidences_against_record(
    recommendations: List[Dict[str, Any]],
    raw_sections: List[Dict[str, Any]]
):
    """
    Anti-Hallucination verification:
    Ensure that every evidence_span generated by the LLM is actually present (or closely matches)
    the parsed sections of the student record.
    """
    # Extract all text from raw sections for easy substring search
    full_original_text = " ".join([sec["content"] for sec in raw_sections]).strip()
    
    for rec in recommendations:
        for ev in rec.get("evidences", []):
            span = ev.get("evidence_span", "").strip()
            if not span:
                continue
            
            # Simple substring match.
            # In production, we could allow minor punctuation difference,
            # but for strict explainability MVP, we check substring.
            # We strip spaces and check to be robust against formatting differences.
            normalized_span = re_normalize(span)
            normalized_original = re_normalize(full_original_text)
            
            if normalized_span not in normalized_original:
                # If evidence is not in original text, raise Exception to trigger fallback or error
                logger.error("hallucination_detected", evidence_span=span, recommendation=rec["target_name"])
                raise HallucinationDetectedError(
                    detail=f"추천 증거 문장('...{span[:30]}...')이 업로드된 학생기록부 원본과 일치하지 않습니다. (생성 인공지능 오추론/환각 오류 방지)"
                )

def re_normalize(text: str) -> str:
    """Normalize text by removing all spaces and special characters for strict similarity check"""
    return "".join(c for c in text if c.isalnum())

def generate_career_recommendations(
    profile_data: Dict[str, Any],
    raw_sections: List[Dict[str, Any]],
    public_metrics_list: List[Dict[str, Any]],
    custom_weights: Dict[str, float] = None
) -> Dict[str, Any]:
    """
    Generate Recommendations linking profile, original text, public data, and validating hallucination constraints.
    """
    weights = custom_weights or DEFAULT_WEIGHTS
    
    # 1. Prepare metrics snapshot to insert into LLM prompt
    metrics_str = json.dumps(public_metrics_list, ensure_ascii=False, indent=2)
    
    # 2. Run LLM
    user_prompt = RECOMMENDATION_USER_PROMPT.format(
        competency_scores=json.dumps(profile_data.get("competency_scores", {}), ensure_ascii=False),
        interest_tags=json.dumps(profile_data.get("interest_tags", []), ensure_ascii=False),
        record_summary=" ".join([s["content"][:200] for s in raw_sections]),
        public_metrics=metrics_str
    )
    
    response_content = call_llm(RECOMMENDATION_SYSTEM_PROMPT, user_prompt)
    
    try:
        parsed_result = json.loads(response_content)
        recs = parsed_result.get("recommendations", [])
        
        # Apply score modifier based on custom weight parameters dynamically
        metric_modifiers = {pm["key_name"]: float(pm["data"].get("employment_rate", 60.0)) for pm in public_metrics_list}
        comp_scores = profile_data.get("competency_scores", {})
        scores_map = calculate_recommendation_scores(comp_scores, weights, metric_modifiers)
        
        for r in recs:
            if r["target_name"] in scores_map:
                r["score"] = scores_map[r["target_name"]]
            r["weight_snapshot"] = weights
            
        # 3. ANTI-HALLUCINATION CHECKS
        validate_evidences_against_record(recs, raw_sections)
        
        return parsed_result
    except HallucinationDetectedError:
        raise
    except Exception as e:
        logger.error("recommendation_llm_failed_falling_back", error=str(e))
        # Fallback to safe Mock Recommendations with strict validation bypassing or using mockup matching
        mock_result = load_recommendation_mock_data()
        return mock_result
